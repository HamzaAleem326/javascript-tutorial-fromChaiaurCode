// Getting Started with JavaScript Printing:

// 1. Install an IDE (Integrated Development Environment):
// An IDE is software for writing and running code. 
// The most popular IDE is VS Code (Visual Studio Code).
// - Download VS Code from https://code.visualstudio.com/.
// - Install it by following the instructions for your operating system.

// 2. Create a JavaScript File:
// - Open VS Code.
// - Create a new file with the extension `.js` (e.g., `main.js` i prefer script.js or main.js because it ).
// - The `.js` extension tells VS Code that the file contains JavaScript code.

// 3. Write and Print Your First Line of Code:
console.log("Hamza Aleem"); // you can write anything you want
// `console.log()` is used to print messages or data to the console (terminal or browser).

// 4. How to Execute JavaScript Code:

// Option 1: Run in a Browser:
// - Open your browser (e.g., Chrome).
// - Open the Developer Tools Console (Ctrl+Shift+J on Windows, Cmd+Option+J on Mac).
// - Paste the JavaScript code (e.g., `console.log("Hello, Browser!")`) and press Enter.
// - The output will appear in the console.

// Option 2: Run in a Terminal Using Node.js:
// - Install Node.js from https://nodejs.org/.
// - To check if Node.js is installed, open the terminal and type:
//   node -v
// - If Node.js is installed, it will show the version (e.g., `v18.0.0`).

// Run your JavaScript file in the terminal:
// - Save your `.js` file (e.g., `example.js`) in a folder.
// - Open the terminal in VS Code (Ctrl+` or View > Terminal).
// - Navigate to the folder containing the file using the `cd` command:
//   Example: cd path/to/your/folder
// - Run the file using the `node` command:
//   node example.js
// - Output: The message inside `console.log()` will be displayed in the terminal.

console.log("Hello, Terminal!");

// Example of Multiple Console Logs:
console.log("This is the first line.");
console.log("This is the second line.");
console.log("This is how we debug code!");
console.log("Node.js allows us to run JavaScript outside a browser.");

// 5. Summary of Steps:
// - Install VS Code and Node.js.
// - Write your code in a `.js` file.
// - Run it in either the browser console or terminal using Node.js.
